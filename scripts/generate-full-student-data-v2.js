const prisma = require('../src/prismaClient');
const evaluationService = require('../src/services/EvaluationService');
const supabase = require('../supabase/supabase.js');

const DEFAULT_ELO = 1200;
const MIN_ELO = 750;
const MAX_ELO = 3000;

function clampElo(value) {
    const parsed = Number(value);
    if (Number.isFinite(parsed) && parsed >= MIN_ELO && parsed <= MAX_ELO) return parsed;
    return DEFAULT_ELO;
}

function determineUserKFactor(elo) {
    const e = clampElo(elo);
    if (e < 1000) return 40;
    if (e < 1200) return 30;
    if (e < 1400) return 20;
    if (e < 1600) return 15;
    if (e < 1800) return 12;
    if (e < 2000) return 10;
    return 8;
}

function calculateExpectedScore(userElo, questionElo) {
    return 1 / (1 + Math.pow(10, -(userElo - questionElo) / 400));
}

function getGradeMultiplier(grade) {
    return 0.5 + (grade / 100) * 0.5;
}

const WINDOW_1_START = new Date('2026-03-26T00:00:00.000Z');
const WINDOW_1_END = new Date('2026-03-29T23:59:59.999Z');
const WINDOW_2_START = new Date('2026-04-08T00:00:00.000Z');
const WINDOW_2_END = new Date('2026-04-09T23:59:59.999Z');

function randomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randomFloat(min, max) {
    return Math.random() * (max - min) + min;
}

async function cleanupAndRegenerate() {
    console.log('[Cleanup] Removing previously generated data...\n');

    try {
        const students = await prisma.user.findMany({
            where: { role: 'STUDENT' },
            select: { id: true, name: true },
        });

        let cleanupCount = 0;

        for (const student of students) {
            const hasQuestionnaire = await prisma.evaluationQuestionnaire.findFirst({
                where: { userId: student.id },
            });

            // Only cleanup students who were generated by our script (not original data)
            // We identify them by checking if they have questionnaire but were NOT in original 10
            const originalStudents = [
                'Sri Intan Ivana Pasaribu',
                'Jelita Sibarani',
                'Yosep Mangadu Simatupang',
                'Elkana Sitorus',
                'Wesly Fery Wanda Ambarita',
                'Marshall Manurung',
                'Grace Evelin Siallagan',
                'Maharani Sitorus',
                'Kevin Aditia',
                'Ralphael Siahaan',
            ];

            const isOriginal = originalStudents.includes(student.name);

            if (hasQuestionnaire && !isOriginal) {
                // Delete questionnaire
                await prisma.evaluationQuestionnaire.deleteMany({
                    where: { userId: student.id },
                });

                // Delete chapters 1-8 completions
                const chapters1to8 = await prisma.chapter.findMany({
                    where: { level: { lte: 8 } },
                    select: { id: true },
                });

                await prisma.userChapter.deleteMany({
                    where: {
                        userId: student.id,
                        chapterId: { in: chapters1to8.map(c => c.id) },
                    },
                });

                // Delete assessments
                await prisma.assessmentAttempt.deleteMany({
                    where: {
                        userId: student.id,
                        submittedAt: { gte: WINDOW_1_START, lte: WINDOW_2_END },
                    },
                });

                // Delete sessions
                await prisma.userSession.deleteMany({
                    where: {
                        userId: student.id,
                        OR: [
                            { loginAt: { gte: WINDOW_1_START, lte: WINDOW_1_END } },
                            { loginAt: { gte: WINDOW_2_START, lte: WINDOW_2_END } },
                        ],
                    },
                });

                // Reset user ELO and points
                await prisma.user.update({
                    where: { id: student.id },
                    data: {
                        elo: DEFAULT_ELO,
                        points: 750,
                    },
                });

                // Clear Supabase summary
                await supabase
                    .from('student_summaries')
                    .update({
                        chapters_completed: 0,
                        assessments_submitted: 0,
                        badges_earned: 1,
                        avg_grade: null,
                        total_points_earned: 0,
                        sdt_autonomy_likert: null,
                        sdt_competence_likert: null,
                        sdt_relatedness_likert: null,
                        sdt_overall_likert: null,
                        engagement_behavioral_likert: null,
                        engagement_cognitive_likert: null,
                        engagement_emotional_likert: null,
                        engagement_overall_likert: null,
                        global_overall_likert: null,
                        updated_at: new Date().toISOString(),
                    })
                    .eq('user_id', student.id);

                cleanupCount++;
            }
        }

        console.log(`✅ Cleaned up ${cleanupCount} students\n`);

        // Now regenerate
        await generateData();
    } catch (err) {
        console.error('[Cleanup] Fatal error:', err.message);
        console.error(err.stack);
    } finally {
        await prisma.$disconnect();
        process.exit(0);
    }
}

async function generateData() {
    console.log('[Generate] Starting data generation (backend formula match)...\n');

    try {
        const students = await prisma.user.findMany({
            where: { role: 'STUDENT' },
            select: { id: true, name: true, studentId: true, elo: true, points: true },
        });

        const chapters = await prisma.chapter.findMany({
            where: { level: { lte: 8 } },
            orderBy: { level: 'asc' },
        });

        console.log(`Found ${students.length} students, ${chapters.length} chapters (1-8)\n`);

        let processedCount = 0;
        let skippedCount = 0;

        for (const student of students) {
            const hasQuestionnaire = await prisma.evaluationQuestionnaire.findFirst({
                where: { userId: student.id },
            });

            if (hasQuestionnaire) {
                console.log(`⏭ Skipped (has questionnaire): ${student.name}`);
                skippedCount++;
                continue;
            }

            console.log(`\n📝 Processing: ${student.name}`);

            const skillLevel = randomFloat(0.35, 0.85);

            // Generate sessions
            const sessionsToCreate = randomInt(2, 8);
            const sessionDates = [];
            const availableDates = [
                new Date('2026-03-26T10:00:00.000Z'),
                new Date('2026-03-27T10:00:00.000Z'),
                new Date('2026-03-28T10:00:00.000Z'),
                new Date('2026-03-29T10:00:00.000Z'),
                new Date('2026-04-08T10:00:00.000Z'),
                new Date('2026-04-09T10:00:00.000Z'),
            ];
            const shuffledDates = availableDates.sort(() => Math.random() - 0.5);
            for (let i = 0; i < Math.min(sessionsToCreate, shuffledDates.length); i++) {
                sessionDates.push(shuffledDates[i]);
            }

            const createdSessions = [];
            for (let i = 0; i < sessionDates.length; i++) {
                const loginAt = sessionDates[i];
                const durationMin = randomInt(10, 40);
                const durationSec = durationMin * 60;
                const logoutAt = new Date(loginAt.getTime() + durationSec * 1000);

                await prisma.userSession.create({
                    data: {
                        userId: student.id,
                        loginAt,
                        logoutAt,
                        lastActiveAt: logoutAt,
                        durationSec,
                    },
                });
                createdSessions.push({ loginAt, logoutAt });
            }
            console.log(`  ✓ Created ${createdSessions.length} sessions`);

            // Generate chapters and assessments
            let currentElo = student.elo || DEFAULT_ELO;
            let totalPointsEarned = 0;
            let totalCorrect = 0;
            let totalQuestions = 0;
            const allGrades = [];

            for (const chapter of chapters) {
                const chapterSession = createdSessions[Math.floor(Math.random() * createdSessions.length)];
                const timeFinished = new Date(chapterSession.logoutAt.getTime() + randomInt(5, 60) * 60 * 1000);

                // Assessment: 4 objective + 1 true/false (counted) + 1 essay (not counted)
                const NUM_SCORING_QUESTIONS = 5;
                const chapterAccuracy = Math.max(0.3, Math.min(0.95, skillLevel + randomFloat(-0.15, 0.15)));
                const correctCount = Math.round(NUM_SCORING_QUESTIONS * chapterAccuracy);

                // Generate question ELOs
                const questionElos = [];
                for (let q = 0; q < NUM_SCORING_QUESTIONS; q++) {
                    const qElo = Math.max(MIN_ELO, Math.min(MAX_ELO, 
                        currentElo + randomInt(-200, 200)
                    ));
                    questionElos.push(qElo);
                }

                // Calculate ELO delta per question (backend formula)
                let chapterEloDeltaRaw = 0;
                let chapterPoints = 0;

                for (let q = 0; q < NUM_SCORING_QUESTIONS; q++) {
                    const isCorrect = q < correctCount;
                    const expectedScore = calculateExpectedScore(currentElo, questionElos[q]);
                    const actualScore = isCorrect ? 1 : 0;
                    
                    const K = determineUserKFactor(currentElo);
                    const eloChange = K * (actualScore - expectedScore);
                    
                    const basePoints = 40;
                    const difficulty = 1 - expectedScore;
                    const points = Math.round(basePoints * difficulty * (isCorrect ? 1 : 0.1));
                    
                    chapterEloDeltaRaw += eloChange;
                    chapterPoints += Math.max(0, points);
                }

                // Apply grade multiplier
                const grade = Math.round((correctCount / NUM_SCORING_QUESTIONS) * 100);
                const gradeMultiplier = getGradeMultiplier(grade);
                chapterEloDeltaRaw *= gradeMultiplier;

                // Provisional user protection
                if (currentElo <= MIN_ELO && chapterEloDeltaRaw < 0) {
                    chapterEloDeltaRaw = 0;
                }

                const finalEloDelta = Math.round(chapterEloDeltaRaw);
                let newElo = Math.max(MIN_ELO, currentElo + finalEloDelta);

                // Soft cap: gradually reduce ELO gains as approaching realistic ceiling
                // Uses sigmoid-like curve instead of hard cap for natural distribution
                if (newElo > 1100) {
                    const excess = newElo - 1100;
                    const reductionFactor = 1 / (1 + Math.exp(0.02 * excess)); // Smooth decay
                    newElo = Math.round(1100 + excess * reductionFactor);
                }

                // Create chapter completion
                await prisma.userChapter.create({
                    data: {
                        userId: student.id,
                        chapterId: chapter.id,
                        isStarted: true,
                        isCompleted: true,
                        materialDone: true,
                        assessmentDone: true,
                        assignmentDone: randomInt(0, 1) === 1,
                        assessmentGrade: grade,
                        assessmentEloDelta: finalEloDelta,
                        assessmentPointsEarned: chapterPoints,
                        timeStarted: chapterSession.loginAt,
                        timeFinished,
                        assessmentAnswer: JSON.stringify({
                            correctAnswers: correctCount,
                            totalQuestions: NUM_SCORING_QUESTIONS,
                            essayCompleted: true,
                        }),
                        currentDifficulty: newElo < 1000 ? 'BEGINNER' : 
                                          newElo < 1200 ? 'BASIC_UNDERSTANDING' :
                                          newElo < 1400 ? 'DEVELOPING_LEARNER' : 'INTERMEDIATE',
                    },
                });

                // Create assessment
                await prisma.assessmentAttempt.create({
                    data: {
                        userId: student.id,
                        chapterId: chapter.id,
                        status: 'SUBMITTED',
                        submittedAt: timeFinished,
                        grade,
                        pointsEarned: chapterPoints,
                        newDifficulty: newElo < 1000 ? 'BEGINNER' : 
                                      newElo < 1200 ? 'BASIC_UNDERSTANDING' :
                                      newElo < 1400 ? 'DEVELOPING_LEARNER' : 'INTERMEDIATE',
                        currentUserElo: newElo,
                        courseEloStart: currentElo,
                        courseEloEnd: newElo,
                        rawEloDelta: finalEloDelta,
                        correctAnswers: correctCount,
                        totalQuestions: NUM_SCORING_QUESTIONS,
                        instruction: `Complete the assessment for chapter ${chapter.level}: ${chapter.name}`,
                        objectiveAnswered: NUM_SCORING_QUESTIONS,
                        objectiveCorrect: correctCount,
                    },
                });

                totalPointsEarned += chapterPoints;
                totalCorrect += correctCount;
                totalQuestions += NUM_SCORING_QUESTIONS;
                allGrades.push(grade);

                console.log(`    Ch${chapter.level}: grade=${grade}%, ${correctCount}/${NUM_SCORING_QUESTIONS}, eloΔ=${finalEloDelta}, pts=${chapterPoints}`);

                currentElo = newElo;
            }

            const avgGrade = Math.round(allGrades.reduce((a, b) => a + b, 0) / allGrades.length);
            const overallAccuracy = totalCorrect / totalQuestions;

            console.log(`  📊 avgGrade=${avgGrade}%, accuracy=${(overallAccuracy * 100).toFixed(1)}%, pts=${totalPointsEarned}, elo=${currentElo}`);

            // Update user
            await prisma.user.update({
                where: { id: student.id },
                data: {
                    elo: currentElo,
                    points: (student.points || 0) + totalPointsEarned,
                },
            });

            // Generate Likert scores
            const performanceScore = (avgGrade / 100) * 0.4 + overallAccuracy * 0.4 + 
                                     Math.max(0, Math.min(1, 0.5 + (currentElo - DEFAULT_ELO) / 400)) * 0.2;

            const generateLikertScore = (basePerformance, variance = 1.0) => {
                const base = 2 + basePerformance * 2.5;
                const variation = randomFloat(-variance, variance);
                return Math.max(1, Math.min(5, Math.round(base + variation)));
            };

            const q1Autonomy = generateLikertScore(performanceScore, 0.8);
            const q2Competence1 = generateLikertScore((avgGrade / 100) * 0.6 + overallAccuracy * 0.4, 0.7);
            const q3Competence2 = generateLikertScore((avgGrade / 100) * 0.5 + overallAccuracy * 0.5, 0.7);
            const q4Relatedness = generateLikertScore(randomFloat(0.3, 0.7), 1.0);
            const q5Behavioral = generateLikertScore(performanceScore * 0.7 + 0.3, 0.8);
            const q6Cognitive = generateLikertScore(performanceScore, 0.9);
            const q7Emotional = generateLikertScore(performanceScore * 0.8 + 0.2, 1.0);
            const q8Overall = generateLikertScore(performanceScore, 0.7);

            console.log(`  📝 Likert: ${q1Autonomy},${q2Competence1},${q3Competence2},${q4Relatedness} | ${q5Behavioral},${q6Cognitive},${q7Emotional},${q8Overall}`);

            let submittedAt = new Date(WINDOW_2_END.getTime() - randomInt(1, 24) * 60 * 60 * 1000);
            if (submittedAt < WINDOW_1_START || isNaN(submittedAt.getTime())) {
                submittedAt = new Date(WINDOW_1_START.getTime() + randomInt(1, 12) * 60 * 60 * 1000);
            }

            await prisma.evaluationQuestionnaire.create({
                data: {
                    userId: student.id,
                    submittedAt,
                    q1Autonomy,
                    q2Competence1,
                    q3Competence2,
                    q4Relatedness,
                    q5Behavioral,
                    q6Cognitive,
                    q7Emotional,
                    q8Overall,
                },
            });

            // Sync to Supabase with caps
            const { start, end } = evaluationService.toDateRange();
            const summary = await evaluationService.computeSummary(student.id, start, end);
            const payload = evaluationService.toSummaryPayload(student.id, summary);

            // Cap to 8
            payload.chapters_completed = Math.min(payload.chapters_completed || 0, 8);
            payload.assessments_submitted = Math.min(payload.assessments_submitted || 0, 8);

            const { error } = await supabase
                .from('student_summaries')
                .upsert(payload, { onConflict: 'user_id' });

            if (error) {
                console.error(`  ❌ Supabase error: ${error.message}`);
            } else {
                console.log(`  ✓ Synced (chapters=${payload.chapters_completed}, assessments=${payload.assessments_submitted})`);
            }

            processedCount++;
        }

        console.log(`\n✅ Done! Processed: ${processedCount}, Skipped: ${skippedCount}`);
    } catch (err) {
        console.error('[Generate] Error:', err.message);
        console.error(err.stack);
    }
}

cleanupAndRegenerate();
